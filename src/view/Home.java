    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        new Thread() {
            @Override
            public void run() {
                if (radio_price.isSelected()) {
                    UpdatePrice price = new UpdatePrice();
                    double novoValor = 0.0;
                    double desconto = Double.parseDouble(tf_desconto.getText());
                    
                    if (check_determinarValor.isSelected() && !"".equals(tf_codigo.getText())) {
                        novoValor = Double.parseDouble(JOptionPane.showInputDialog("Digite o Novo Valor"));
                        desconto = 0;
                        tf_log.setText("AGUARDE... ATUALIZANDO PREÇO");
                        tf_log.setText(price.update_price(tf_codigo.getText(), desconto,
                                true, novoValor));
                    } else {
                        tf_log.setText("AGUARDE... ATUALIZANDO PREÇO");
                        tf_log.setText(price.update_price(tf_codigo.getText(), desconto,
                                false, 0));
                    }
                    System.out.println(!"".equals(tf_codigo.getText()));
                    System.out.println(tf_codigo.getText());
                    System.out.println(novoValor);

                } else if (radio_stock.isSelected()) {
                    UpdateStock stock = new UpdateStock();
                    double novaQuantidade = 0.0;
                    boolean determinarEstoque = false;
                    if (check_determinarEstoque.isSelected() && !"".equals(tf_codigo.getText())) {
                        novaQuantidade = Double.parseDouble(JOptionPane.showInputDialog("Digite a nova quantidade em estoque"));
                        determinarEstoque = true;
                    }
                    tf_log.setText("AGUARDE... ATUALIZANDO ESTOQUE");
                    tf_log.setText(stock.update_stock(tf_codigo.getText(), determinarEstoque, novaQuantidade));

                } else if (radio_app.isSelected()) {
                    tf_log.setText("EM BREVE A ATUALIZAÇÃO DO APLICATIVO ESTARÁ DISPONIVEL");

                } else if (radio_referencia.isSelected() && !"".equals(tf_codigo.getText())) {
                    Referencias ref = new Referencias();
                    tf_log.setText("Atualizando Referencia Aguarde....");
                    tf_log.setText(ref.update_query(tf_codigo.getText()));

                } else {
                    tf_log.setText("Para atualizar a referencia, Digite o codigo do Produto");
                }

            }
        }.start();

    }//GEN-LAST:event_jButton1ActionPerformed

