    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        cadastraProduto cadastra = new cadastraProduto();

        new Thread() {
            @Override
            public void run() {
                int cont = 0;
                tf_logcadastro.setText("Aguarde Cadastrando Produtos");
                String log = "";
                if (!"".equals(codigo1.getText())) {
                    log += cadastra.criarPostWoocommerce(codigo1.getText()) + "\n\n";
                    tf_logcadastro.setText(log);
                    cont++;
                }
                if (!"".equals(codigo2.getText())) {
                    log += cadastra.criarPostWoocommerce(codigo2.getText()) + "\n\n";
                    tf_logcadastro.setText(log);
                    cont++;
                }
                if (!"".equals(codigo3.getText())) {
                    log += cadastra.criarPostWoocommerce(codigo3.getText()) + "\n\n";
                    tf_logcadastro.setText(log);
                    cont++;
                }
                if (!"".equals(codigo4.getText())) {
                    log += cadastra.criarPostWoocommerce(codigo4.getText()) + "\n\n";
                    tf_logcadastro.setText(log);
                    cont++;
                }
                if (!"".equals(codigo5.getText())) {
                    log += cadastra.criarPostWoocommerce(codigo5.getText()) + "\n\n";
                    tf_logcadastro.setText("");
                    tf_logcadastro.setText(log);
                    cont++;
                }
                log = "";
                if (cont == 0) {
                    tf_logcadastro.setText("Para cadastrar informe ao menos um código válido");
                }
            }
        }.start();
    }//GEN-LAST:event_jButton6ActionPerformed

